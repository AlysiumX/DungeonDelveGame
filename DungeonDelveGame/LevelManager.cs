		public void LoadContent( ContentManager content )
		{
			_floorTile = content.Load<Texture2D>( "floor1" );
			_wallTile = content.Load<Texture2D>( "wall1" );
		}

		public void Draw( SpriteBatch spriteBatch )
		{
			for( var x = 0; x<level.GetLength( 1 ); x++ )
			{
				for( var y = 0; y<level.GetLength( 0 ); y++ )
				{
					if( level[y, x] == 0 )
						spriteBatch.Draw( _floorTile, new Vector2( x * textureSize, y * textureSize ), Color.White );
					if( level[y, x] == 1 )
						spriteBatch.Draw( _wallTile, new Vector2( x * textureSize, y * textureSize ), Color.White );
				}
			}

